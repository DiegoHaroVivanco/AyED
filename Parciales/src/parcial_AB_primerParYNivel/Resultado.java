package parcial_AB_primerParYNivel;

public class Resultado {

	private int nodo;
	private int nivel;
	
	public Resultado() {
		this.nodo = 0;
		this.nivel = -1;
	}

	public int getNodo() {
		return nodo;
	}

	public void setNodo(int nodo) {
		this.nodo = nodo;
	}

	public int getNivel() {
		return nivel;
	}

	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	
	
}
